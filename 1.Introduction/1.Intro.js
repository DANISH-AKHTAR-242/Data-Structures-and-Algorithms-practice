/**
 * Data structures are of two types: linear and non-linear.
 * Linear data structures include arrays, linked lists, stacks, and queues.
 * Non-linear data structures include trees and graphs.
 *
 *
 * 1.Time Complexity: Time complexity is a measure of the amount of time an algorithm takes to complete as a function of the size of the input. It is usually expressed using
 *  Big O notation, which describes the upper bound of the growth rate of an algorithm. For example, an algorithm with a time complexity of O(n) means that the time it takes to complete grows linearly with the size of the input.
 *
 * 2.Space Complexity: Space complexity is a measure of the amount of memory an algorithm uses as a function of the size of the input. Like time complexity, it is also expressed using Big O notation. For example, an algorithm with a space complexity of O(1) means that it uses a constant amount of memory regardless of the size of the input.
 *
 * Understanding time and space complexity is crucial for designing efficient algorithms and data structures. It helps developers choose the right approach for solving problems and optimizing performance.
 */

